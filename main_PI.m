%% ====== Helper Functions ======

function k = forward_kinematics(q, L1, L2, O1, O2)
    % End-effector Cartesian position
    x1 = L1*cos(q(1)) + L2*cos(q(1)+q(2)) + O1;
    x2 = L1*sin(q(1)) + L2*sin(q(1)+q(2)) + O2;
    k = [x1; x2];
end

function R = rot_matrix(angle)
    % 2D rotation matrix exp(J*angle) where J = [0 -1; 1 0]
    R = [cos(angle), -sin(angle);
         sin(angle),  cos(angle)];
end

function Jac = robot_jacobian(q, L1, L2)
    % Analytic Jacobian of end-effector w.r.t. joint angles
    Jac = [-L1*sin(q(1)) - L2*sin(q(1)+q(2)),  -L2*sin(q(1)+q(2));
            L1*cos(q(1)) + L2*cos(q(1)+q(2)),   L2*cos(q(1)+q(2))];
end